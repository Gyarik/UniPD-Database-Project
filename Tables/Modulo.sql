	id_modulo INT NOT NULL,
	id_prog INT NOT NULL,
	UNIQUE (id_modulo),
	PRIMARY KEY (id_modulo, id_prog),
	FOREIGN KEY (id_prog) REFERENCES Progetto(id_prog) ON UPDATE CASCADE ON DELETE CASCADE
);

